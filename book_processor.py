# Vocabulary Extraction (Python, no AI)
# ===========================================================================

def extract_vocabulary(section_text: str) -> List[Dict[str, str]]:
    """
    Extract vocabulary pairs from section text using pattern matching.
    Looks for patterns like:
      "1  baako" or "baako = one" or "baako (one)" or "one = baako"
    Returns list of {"local": "...", "english": "..."}
    """
    pairs = []

    # Pattern 1: Number followed by word: "1  baako"
    matches = re.findall(r'(\d+)\s+([^\d\s]{2,30})', section_text)
    for num, word in matches:
        word = word.strip().rstrip('.,;:')
        if len(word) >= 2 and not word.isdigit():
            pairs.append({"local": word, "english": num})

    # Pattern 2: word = english or english = word
    matches = re.findall(r'([^\d\s]{2,30})\s*=\s*([^\d\s]{2,30})', section_text)
    for a, b in matches:
        a, b = a.strip().rstrip('.,;:'), b.strip().rstrip('.,;:')
        if len(a) >= 2 and len(b) >= 2:
            # Determine which is local and which is English
            if re.search(r'[ɛɔƆɛŋŋ]', a) or not all(ord(c) < 128 for c in a):
                pairs.append({"local": a, "english": b})
            elif re.search(r'[ɛɔƆɛŋŋ]', b) or not all(ord(c) < 128 for c in b):
                pairs.append({"local": b, "english": a})
            else:
                pairs.append({"local": a, "english": b})

    # Pattern 3: "word (english)" or "english (word)"
    matches = re.findall(r'([^\d\s]{2,30})\s*\(([^)]+)\)', section_text)
    for a, b in matches:
        a, b = a.strip().rstrip('.,;:'), b.strip().rstrip('.,;:')
        if len(a) >= 2 and len(b) >= 2 and len(b) < 50:
            pairs.append({"local": a, "english": b})

    # Deduplicate by local word
    seen = set()
    unique = []
    for pair in pairs:
        key = pair["local"].lower()
        if key not in seen:
            seen.add(key)
            unique.append(pair)

    return unique[:50]  # Limit to 50 pairs per section


def extract_topic_from_section(section_text: str, language_name: str) -> str:
    """Extract a topic name from section text."""
    lines = section_text.strip().split('\n')
    for line in lines[:5]:
        line = line.strip()
        if 5 < len(line) < 120:
            topic = re.sub(r'^[\d]+[\.\)]\s*', '', line)
            topic = topic.strip().rstrip('.:')
            if topic and len(topic) > 3:
                return f"{language_name}: {topic}"
    first_line = lines[0].strip()[:80] if lines else "Language lesson"
    return f"{language_name}: {first_line}"


def build_vocabulary_table(pairs: List[Dict[str, str]]) -> str:
    """Build a formatted vocabulary table from extracted pairs."""
    if not pairs:
        return ""
    lines = []
    for pair in pairs:
        lines.append(f"{pair['local']} = {pair['english']}")
    return "\n".join(lines)


