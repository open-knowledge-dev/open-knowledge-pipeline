def generate_wrapper_with_groq(system_prompt: str, user_prompt: str) -> str:
    """Generate English wrapper using Groq."""
    if not GROQ_API_KEY:
        return ""
    print(f"    [Groq] Writing English wrapper...")
    sys.stdout.flush()
    headers = {"Authorization": f"Bearer {GROQ_API_KEY}", "Content-Type": "application/json"}
    payload = {
        "model": "llama-3.1-8b-instant",
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
        "temperature": 0.75,
        "max_tokens": 1500,
    }
    try:
        response = requests.post(GROQ_API_URL, json=payload, headers=headers, timeout=REQUEST_TIMEOUT)
        if response.status_code == 200:
            content = response.json()["choices"][0]["message"]["content"]
            print(f"    Generated {len(content)} chars")
            sys.stdout.flush()
            return content
        else:
            print(f"    Groq error: {response.status_code}")
            return ""
    except Exception as e:
        print(f"    Groq exception: {e}")
        return ""


def generate_wrapper_with_mistral(system_prompt: str, user_prompt: str) -> str:
    """Generate English wrapper using Mistral (fallback)."""
    if not MISTRAL_API_KEY:
        return ""
    print(f"    [Mistral] Writing English wrapper...")
