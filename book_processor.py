def extract_pdf_text(pdf_path: str) -> str:
    """Extract all text from a PDF file using pdfplumber."""
    try:
        import pdfplumber
    except ImportError:
        import subprocess
        subprocess.check_call([sys.executable, "-m", "pip", "install", "pdfplumber"])
        import pdfplumber

    print(f"  Extracting text from PDF: {pdf_path}")
    sys.stdout.flush()

    full_text = []
    try:
        with pdfplumber.open(pdf_path) as pdf:
            total_pages = len(pdf.pages)
            print(f"  Total pages: {total_pages}")
            for i, page in enumerate(pdf.pages):
                text = page.extract_text()
                if text:
                    full_text.append(text)
                if (i + 1) % 10 == 0:
                    print(f"  Extracted page {i + 1}/{total_pages}")
                    sys.stdout.flush()
    except Exception as e:
        print(f"  PDF extraction error: {e}")
        return ""

    combined = "\n\n".join(full_text)
    print(f"  Total extracted: {len(combined)} characters")
    return combined


def split_into_sections(text: str, max_chars: int = 2000) -> List[str]:
    """Split text into manageable sections by paragraphs."""
    paragraphs = re.split(r'\n\s*\n', text)
    sections = []
    current_section = ""

    for para in paragraphs:
        para = para.strip()
        if not para:
            continue
        if len(current_section) + len(para) > max_chars and current_section:
            sections.append(current_section.strip())
            current_section = para
        else:
            current_section = f"{current_section}\n\n{para}" if current_section else para

    if current_section.strip():
        sections.append(current_section.strip())

    sections = [s for s in sections if len(s) > 100]
    print(f"  Split into {len(sections)} sections")
    return sections


