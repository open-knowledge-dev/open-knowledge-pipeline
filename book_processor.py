"""
Book Knowledge Processor — v3.0
================================
Extracts language facts from PDFs and generates lessons using
a template-based approach — AI writes the English wrapper,
Python inserts the exact target-language facts.

Zero hallucination risk. Works for any language.

How it works:
1. PDF text is extracted and split into sections
2. Python extracts vocabulary pairs (local word + English meaning)
3. AI writes an English introduction, teaching tips, and conclusion
4. Python inserts the exact vocabulary between AI-written sections
5. The target-language words are NEVER generated by AI
6. All output goes to the PRIVATE knowledge repo
"""

