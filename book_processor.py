def build_lesson_prompt(vocab_table: str, language_name: str, topic: str) -> Tuple[str, str]:
    """
    Build prompts for AI to write the English wrapper around vocabulary.
    The AI NEVER writes target-language words — only English explanations.
    """
    system_prompt = (
        f"You are a native {language_name} speaker and a warm, encouraging language teacher. "
        "Your job is to write an English-language lesson INTRODUCTION and CONCLUSION. "
        "You will NOT write any target-language words — those are provided separately.\n\n"
        "CRITICAL RULES:\n"
        "1. Write ONLY in English.\n"
        "2. Do NOT write any {language_name} words — not even ones you think you know.\n"
        "3. Write a warm, personal introduction (2-3 paragraphs).\n"
        "4. Write teaching tips and memory tricks (1-2 paragraphs).\n"
        "5. Write an encouraging conclusion (1 paragraph).\n"
        "6. Separate sections with [INTRO], [TIPS], and [CONCLUSION] markers.\n"
        "7. Write in first person as if you grew up speaking this language.\n"
        "8. Never mention AI, language models, or books.\n"
        "9. Do NOT use markdown formatting.\n"
        "10. Write at least 300 words total."
    )

    user_prompt = (
        f"I am creating a {language_name} language lesson about: {topic}\n\n"
        f"The vocabulary words for this lesson are:\n{vocab_table}\n\n"
        "Write ONLY the English introduction, teaching tips, and conclusion. "
        "Use the markers [INTRO], [TIPS], and [CONCLUSION]. "
        f"Do NOT write any {language_name} words. "
        "Refer to 'the words below' or 'the vocabulary table' — "
        "the actual words will be inserted separately."
    )

    return system_prompt, user_prompt


# ===========================================================================
# Markdown Stripping
# ===========================================================================

def strip_markdown(text: str) -> str:
    """Remove all markdown formatting."""
    text = re.sub(r'\*{1,3}([^*]+?)\*{1,3}', r'\1', text)
    text = re.sub(r'_{1,3}([^_]+?)_{1,3}', r'\1', text)
    text = re.sub(r'^#{1,6}\s+', '', text, flags=re.MULTILINE)
    text = re.sub(r'^[\-\*]\s+', '', text, flags=re.MULTILINE)
    text = re.sub(r'```[^`]*```', '', text)
    text = re.sub(r'`([^`]+)`', r'\1', text)
    text = re.sub(r'^[-*_]{3,}\s*$', '', text, flags=re.MULTILINE)
    text = re.sub(r'\n{3,}', '\n\n', text)
    return text.strip()


