# Lesson Assembly (Python inserts facts, AI provides wrapper)
# ===========================================================================

def assemble_lesson(vocab_pairs: List[Dict[str, str]], language_name: str, topic: str) -> str:
    """
    Assemble a complete lesson:
    - AI writes intro, tips, conclusion in English
    - Python inserts the exact vocabulary
    """
    vocab_table = build_vocabulary_table(vocab_pairs)
    if not vocab_table:
        return ""

    system_prompt, user_prompt = build_lesson_prompt(vocab_table, language_name, topic)

    wrapper = ""
    if GROQ_API_KEY:
        wrapper = generate_wrapper_with_groq(system_prompt, user_prompt)
    if not wrapper and MISTRAL_API_KEY:
        wrapper = generate_wrapper_with_mistral(system_prompt, user_prompt)

    if not wrapper:
        return ""

    wrapper = strip_markdown(wrapper)

    # Parse the wrapper sections
    intro_match = re.search(r'\[INTRO\](.*?)(?:\[TIPS\]|\[CONCLUSION\]|\Z)', wrapper, re.DOTALL)
    tips_match = re.search(r'\[TIPS\](.*?)(?:\[CONCLUSION\]|\Z)', wrapper, re.DOTALL)
    conclusion_match = re.search(r'\[CONCLUSION\](.*?)\Z', wrapper, re.DOTALL)

    intro = intro_match.group(1).strip() if intro_match else ""
    tips = tips_match.group(1).strip() if tips_match else ""
    conclusion = conclusion_match.group(1).strip() if conclusion_match else ""

    # If markers weren't found, use the whole wrapper as intro
    if not intro and not tips and not conclusion:
        intro = wrapper
        tips = "Practice these words daily. Say them out loud, use them in sentences, and try them with native speakers."
        conclusion = "Keep practicing, and you will master this beautiful language. Every word you learn connects you more deeply to the culture and people."

    # Build the vocabulary section (PYTHON inserts exact words — no AI)
    vocab_lines = []
    for pair in vocab_pairs:
        vocab_lines.append(f"{pair['local']} — {pair['english']}")
    vocab_section = "\n".join(vocab_lines)

    # Assemble final lesson
    lesson = f"{intro}\n\n"
    lesson += f"Here are the {language_name} words for this lesson:\n\n"
    lesson += f"{vocab_section}\n\n"
    if tips:
        lesson += f"{tips}\n\n"
    if conclusion:
        lesson += f"{conclusion}"

    lesson = re.sub(r'\n{3,}', '\n\n', lesson)

    return lesson


